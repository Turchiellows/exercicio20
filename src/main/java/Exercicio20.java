// IMPORTS

import java.util.Scanner;

public class Exercicio20 {

   public static void main(String[] args) {
      // INSTANTIATE
      Scanner sc = new Scanner(System.in);

      // DECLARATE
      int n;

      // COLLECT DATA
      System.out.print("NA : ");
      n = sc.nextInt();

      // OUTPUT
      for (int i = 1; i <= n; i++) {
         System.out.println(i);
      }

   }
}
